package keylimepie;

import java.io.File;

public class TestUI
{
	public static void main(String args[])
	{
		FileMgrText mgr = new FileMgrText();
		mgr.addDirectory(new File("texts/"));
		
		mgr.load();
		
		for(DecisionObject<String> d : mgr)
		{
			if(d.getPath().endsWith("er.txt"))
				d.setAssigned(Assigning.HIM);
			else
				d.setAssigned(Assigning.ME);
			
			System.out.println(d.getContent());
		}
		
		mgr.save();
	}
}
